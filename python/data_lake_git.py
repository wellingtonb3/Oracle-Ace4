import requests
import io
import pandas as pd
import datetime
import time

# --- CONFIGURATION ---
URL_PAR = "xxxxxxx"

def batch_transform_silver(start_year, end_year):
    """
    Automates the transformation of all Bronze JSON files into Silver Parquet files.
    """
    current_date = datetime.datetime.now()

    for year in range(start_year, end_year + 1):
        for month in range(1, 13):
            # Stop if we reach a future month in 2026
            if year == current_date.year and month > current_date.month:
                break

            bronze_path = f"01_bronze/nytimes/{year}/{month:02d}_archive.json"
            silver_path = f"02_silver/nytimes/{year}/{month:02d}_cleaned.parquet"

            print(f"🔄 Processing {month:02d}/{year}...")

            # 1. Download from Bronze
            response = requests.get(URL_PAR + bronze_path)

            if response.status_code == 200:
                try:
                    raw_json = response.json()
                    articles = raw_json.get('response', {}).get('docs', [])

                    if not articles:
                        print(f"⚠️ No articles found for {month:02d}/{year}. Skipping...")
                        continue

                    # 2. Transform with Pandas
                    df = pd.DataFrame(articles)

                    # 3. Clean and Structure
                    df_clean = pd.DataFrame({
                        "publish_date": pd.to_datetime(df['pub_date']).dt.date,
                        "headline": df['headline'].apply(lambda x: x.get('main') if isinstance(x, dict) else None),
                        "section": df['section_name'],
                        "abstract": df['abstract'],
                        "word_count": df['word_count'],
                        "web_url": df['web_url']
                    })

                    # 4. Save to Parquet Buffer (Memory)
                    parquet_buffer = io.BytesIO()
                    df_clean.to_parquet(parquet_buffer, engine='pyarrow', index=False)
                    parquet_buffer.seek(0)

                    # 5. Upload to Silver Layer
                    print(f"📤 Uploading Parquet to Silver: {silver_path}")
                    res_silver = requests.put(URL_PAR + silver_path, data=parquet_buffer)

                    if res_silver.status_code == 200:
                        print(f"✅ Success: {month:02d}/{year} is Silver!")
                    else:
                        print(f"❌ Upload Error {res_silver.status_code}")

                except Exception as e:
                    print(f"💥 Error processing {month:02d}/{year}: {e}")
            else:
                print(f"❌ Bronze file not found: {bronze_path} (Status {response.status_code})")

            # Short sleep to avoid overwhelming your local network/RAM
            time.sleep(1)

# --- START BATCH PROCESS ---
print("🚀 Starting Massive Silver Transformation (2019-2026)...")
batch_transform_silver(2019, 2026)

