import pandas as pd
import requests
import io

# --- CONFIGURATION ---
URL_PAR = "xxxxxxxxxxx"

def generate_gold_analytics(start_year, end_year):
    all_data = []

    print("🏆 Collecting data from Silver to create GOLD insights...")

    for year in range(start_year, end_year + 1):
        for month in range(1, 13):
            silver_path = f"02_silver/nytimes/{year}/{month:02d}_cleaned.parquet"

            # Download from Silver
            response = requests.get(URL_PAR + silver_path)

            if response.status_code == 200:
                # Read Parquet from memory
                df_month = pd.read_parquet(io.BytesIO(response.content))
                all_data.append(df_month)
            else:
                continue # Skip if month doesn't exist yet

    # 1. Combine all years and months into ONE big table
    full_df = pd.concat(all_data, ignore_index=True)

    # 2. Perform Gold Analysis: Group by Section and Year
    # Example: How many articles per section each year?
    full_df['year'] = pd.to_datetime(full_df['publish_date']).dt.year
    gold_analysis = full_df.groupby(['year', 'section']).size().reset_index(name='article_count')

    # 3. Save the Gold Insight back to OCI
    gold_path = "03_gold/analytics/articles_distribution_2019_2026.csv"
    csv_buffer = io.StringIO()
    gold_analysis.to_csv(csv_buffer, index=False)

    print(f"📤 Uploading Gold Insight to {gold_path}...")
    requests.put(URL_PAR + gold_path, data=csv_buffer.getvalue())

    print("✅ GOLD LAYER UPDATED! Ready for Dashboard.")

# --- RUN GOLD PROCESS ---
generate_gold_analytics(2019, 2024)



