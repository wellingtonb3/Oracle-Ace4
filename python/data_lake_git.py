# Ensure the PAR URL ends with /o/
URL_PAR_BRONZE = "XXXXXX"

def start_archive_ingestion(start_year, end_year):
    """
    Downloads full NYT archives (thousands of articles per month)
    and uploads them to OCI Bronze Layer.
    """
    current_date = datetime.datetime.now()

    for year in range(start_year, end_year + 1):
        for month in range(1, 13):
            # 1. Stop if we reach a future month in 2026
            if year == current_date.year and month > current_date.month:
                break

            print(f"📡 Requesting FULL ARCHIVE for {month:02d}/{year}...")

            # 2. Correct Endpoint for Archive API
            nyt_archive_url = f"https://api.nytimes.com/svc/archive/v1/{year}/{month}.json?api-key={NYT_API_KEY}"

            try:
                # Increased timeout to 90s because Archive files are LARGE (~20MB+)
                response_nyt = requests.get(nyt_archive_url, timeout=90)

                if response_nyt.status_code == 200:
                    # 3. Define the Hive-style path in OCI
                    object_path = f"01_bronze/nytimes/{year}/{month:02d}_archive.json"
                    oci_final_url = URL_PAR_BRONZE + object_path

                    # 4. Upload raw JSON to OCI
                    print(f"📤 Uploading {object_path} to OCI Object Storage...")
                    response_oci = requests.put(oci_final_url, json=response_nyt.json())

                    if response_oci.status_code == 200:
                        print(f"✅ SUCCESS: {object_path} is now in the Data Lake!")
                    else:
                        print(f"❌ OCI UPLOAD ERROR: {response_oci.status_code}")

                elif response_nyt.status_code == 401:
                    print("❌ AUTH ERROR: Check if 'Archive API' is enabled in NYT Portal.")
                    return # Stop script to fix API access

                elif response_nyt.status_code == 429:
                    print("⚠️ RATE LIMIT: NYT is busy. Waiting 60 seconds...")
                    time.sleep(60)
                else:
                    print(f"❌ NYT API ERROR: {response_nyt.status_code}")

            except Exception as e:
                print(f"💥 SYSTEM CRASH: {e}")

            # 5. Mandatory pause to respect NYT's API limits
            # Archive files are big; we don't want to spam the server.
            time.sleep(12)

# --- BOOTSTRAP ---
print("🚀 Starting Data Lake Population (2019-2026)...")
start_archive_ingestion(2019, 2026)



