def consult_news(category="U.S."):
    # 1. Download the Parquet file into memory
    response = requests.get(URL_PAR)

    if response.status_code == 200:
        # 2. Load into Pandas
        df = pd.read_parquet(io.BytesIO(response.content))

        # 3. Filter by 'section' (Category)
        # In NYT, "Internal" news is usually under the "U.S." or "National" section
        filtered_news = df[df['section'] == category]

        print(f"--- Top 10 News in {category} ---")
        print(filtered_news[['publish_date', 'headline']].head(10))
    else:
        print("Error accessing the Data Lake")

